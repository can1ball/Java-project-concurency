package factory;

public class Position {
	// x coordinate in the matrix
	public int x;
	// y coordinate in the matrix
	public int y;

	/**
	 * Constructor. Initializes fields
	 * @param x row coordinate
	 * @param y column coordinate
	 */
	public Position(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
